
public class Stringapp {

	public static void main(String[] args) {
//		Character vs String
		System.out.println("Hello World"); // String
		System.out.println('H'); // Character
		
		System.out.println("Hello world");
		System.out.println("Hello \n World");
		// new line
		
		// escape
		System.out.println("Hello \"World\"");
	}

}
